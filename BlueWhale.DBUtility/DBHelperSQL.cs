        /// 2012-02-21 Add overload to execute a calculation query statement and return the query result (object).
        /// </summary>
        /// <param name="connection">SqlConnection object</param>
        /// <param name="trans">SqlTransaction transaction</param>
        /// <param name="SQLString">Calculation query statement</param>
        /// <returns>Query result (object)</returns>
        public static object GetSingle(SqlConnection connection, SqlTransaction trans, string SQLString, params SqlParameter[] cmdParms)
        {
            using (SqlCommand cmd = new SqlCommand())
            {
                try
                {
                    PrepareCommand(cmd, connection, trans, SQLString, cmdParms);
                    object obj = cmd.ExecuteScalar();
                    cmd.Parameters.Clear();
                    if ((Object.Equals(obj, null)) || (Object.Equals(obj, System.DBNull.Value)))
                    {
                        return null;
                    }
                    else
                    {
                        return obj;
                    }
                }
                catch (System.Data.SqlClient.SqlException e)
                {
                    trans.Rollback();
                    throw e;
                }
            }
        }

